package homeWork.homeWork8;

public class Calculator {
    static public int addition(int first, int second) {
        return first + second;
    }
    static public double addition(double first, double second) {
        return first + second;
    }

    static public int subtraction(int first, int second) {
        return first - second;
    }
    static public double subtraction(double first, double second) {
        return first - second;
    }

    static public int multiplication(int first, int second) {
        return first * second;
    }
    static public double multiplication(double first, double second) {
        return first * second;
    }

    static public double divide (int first, int second) {
        return (double)first / (double)second;
    }
    static public double divide (double first, double second) {
        return first / second;
    }

    static public int balance (int first, int second) {
        return first % second;
    }
    static public double balance (double first, double second) {
        return first % second;
    }
}
