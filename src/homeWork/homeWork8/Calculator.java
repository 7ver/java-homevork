package homeWork.homeWork8;

public class Calculator {
    static public double addition(double first, double second) {
        return first + second;
    }

    static public double subtraction(double first, double second) {
        return first - second;
    }

    static public double multiplication(double first, double second) {
        return first * second;
    }

    static public double divide (double first, double second) {
        return first / second;
    }

    static public double balance (double first, double second) {
        return first % second;
    }
}
